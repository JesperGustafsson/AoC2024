fn next_char(
    chars: &Vec<char>,
    next_horizontal: usize,
    rows: &Vec<&str>,
    next_vertical: usize,
) -> char {
    return rows[next_vertical].chars().nth(next_horizontal).unwrap();
}

fn check(
    chars: &Vec<char>,
    j: usize,
    rows: &Vec<&str>,
    i: usize,
    horizontal: i32,
    vertical: i32,
    log: bool,
) -> i32 {
    let mut horizontal_pos = j as i32;
    let mut vertical_pos = i as i32;
    let mut do_try = false;
    let mut matchee = "SAMX";

    match (horizontal, vertical) {
        (-1, 0) => {
            do_try = j >= 3;
        }
        (1, 0) => {
            do_try = j < chars.len() - 3;
        }
        (0, -1) => {
            do_try = i >= 3;
        }
        (0, 1) => {
            do_try = i < rows.len() - 3;
        }
        (-1, -1) => {
            do_try = i >= 3 && j >= 3;
        }
        (1, 1) => {
            do_try = i < rows.len() - 3 && j < chars.len() - 3;
        }
        (-1, 1) => {
            do_try = i < rows.len() - 3 && j >= 3;
        }
        (1, -1) => {
            do_try = i >= 3 && j < chars.len() - 3;
        }
        _ => (),
    }
    if do_try {
        let c = chars[j];
        horizontal_pos += horizontal;
        vertical_pos += vertical;
        let left_char = next_char(chars, horizontal_pos as usize, rows, vertical_pos as usize);
        horizontal_pos += horizontal;
        vertical_pos += vertical;
        let left_char_2 = next_char(chars, horizontal_pos as usize, rows, vertical_pos as usize);
        horizontal_pos += horizontal;
        vertical_pos += vertical;
        let left_char_3 = next_char(chars, horizontal_pos as usize, rows, vertical_pos as usize);
        let complete = format!("{}{}{}{}", left_char_3, left_char_2, left_char, c);
        if complete == matchee {
            return 1;
        }
    }

    return 0;
}

